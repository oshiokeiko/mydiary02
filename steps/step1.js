// ============================================================
// STEP1：書く＋一覧表示（まだ保存はしない）
//
// この段階で学ぶこと：
//   ・配列     … たくさんの日記を1つの箱にまとめて入れておくもの
//   ・オブジェクト … 日記1件分（タイトル・本文）のまとまり
//   ・繰り返し  … 配列の中身を1件ずつ取り出して画面に並べる
// ============================================================


// 【配列】日記を全部ためておく箱。最初は空っぽ（[] は空の配列）。
// ※この変数はメモリの中だけ。リロードすると消える（保存はSTEP2で足す）。
let diaries = [];


// ============================================================
// 一覧を画面に表示する関数
// ============================================================
function showList() {
  // まず一覧エリアの中身を空にする（毎回まっさらにしてから描き直す）
  $('#diary-list').empty();

  // 【繰り返し】$.each は「配列の中身を1件ずつ順番に処理する」jQueryの命令。
  //   diaries の中の1件を diary という名前で受け取り、{ } の中の処理を繰り返す。
  $.each(diaries, function (index, diary) {

    // 日記1件分のカードのHTMLを文字で組み立てる。
    // ${ } の中に、その日記のタイトルや本文を差し込む。
    //escapeHtml は、記号を「見た目は同じだけど、命令と勘違いされない別の書き方」に置きかるもの。
    const cardHtml = `
      <div class="diary-card bg-slate-800 rounded-xl p-5 border border-slate-700">
        <h3 class="text-lg font-bold mb-2">${escapeHtml(diary.title)}</h3>
        <p class="text-slate-300 text-sm whitespace-pre-wrap">${escapeHtml(diary.body)}</p>
      </div>
    `;

    // 組み立てたカードを一覧エリアの最後に追加する
    $('#diary-list').append(cardHtml);
  });
}


// ============================================================
// ページが読み込まれたら動かす処理
// ============================================================
// $(function () { ... }) は「画面の準備ができてから中身を動かす」というjQueryのお決まりの書き方。
$(function () {

  // 最初に一度、一覧を表示する（最初は空なので何も出ない）
  showList();

  // 「追加する」ボタンが押されたときの動き
  $('#add-btn').on('click', function () {

    // .val() で入力欄に書かれた文字を取り出す。.trim() は前後の空白を取り除く。
    const title = $('#input-title').val().trim();
    const body  = $('#input-body').val().trim();

    // どちらかが空なら、注意を出してここで中断（return で以降をやめる）
    if (!title || !body) {
      alert('タイトルと本文を入力してください。');
      return;
    }

    // 【オブジェクト】日記1件分を { } でまとめて作る。
    const newDiary = {
      title: title,
      body: body
    };

    // 【配列に追加】unshift は「配列の先頭に1件足す」命令。
    // 先頭に足すので、新しい日記が一覧の一番上に表示される。
    // （反対に push を使うと「最後に足す」ので、新しいものが下に出る）
    diaries.unshift(newDiary);

    // 入力欄を空にする（次の日記を書きやすいように）
    $('#input-title').val('');
    $('#input-body').val('');

    // 一覧を表示し直して、足した日記を画面に反映する
    showList();
  });

});


// ============================================================
// おまけ：入力された文字を安全に表示するための小さな関数
// ============================================================
// < や > などの記号をそのまま画面に出すと、まれに表示が壊れることがある。
// それを防ぐために、記号を「ただの文字」に置きかえてから表示する。
// （中身は丸暗記しなくてOK。「安全のためのお守り」くらいの理解で十分）
function escapeHtml(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}
